static void shift_and_copy(volatile uint8_t *dst, uint8_t *src, size_t size) {
    for (size_t i = 0; i < size; i+=4) {
        for (size_t j = 0; j < 4; j++) {
            dst[2*i + j] = src[i + j];
        }
    }
}

static void copy_and_shift(uint8_t *dst, volatile uint8_t *src, size_t size) {
    for (size_t i = 0; i < size; i+=4) {
        for (size_t j = 0; j < 4; j++) {
            dst[i + j] = src[2*i + j];
        }
    }
}

